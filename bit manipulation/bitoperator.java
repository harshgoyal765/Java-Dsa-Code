
public class bitoperator {
    public static void main(String args[]){
        System.out.println((5&6));//and operator
        System.out.println((5|6));//or operator
        System.out.println((5^6));//xor operator
        System.out.println((~0));//not operator
        System.out.println((5<<2));//binary left shift operator
        System.out.println((5>>2));//binary right shift operator
    }
    
}
