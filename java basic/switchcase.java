
import java.util.*;
public class switchcase {
       public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println("enter number");
        int a=sc.nextInt();
        switch(a){
            case 1 : System.out.println("hello1");break;
            case 2 : System.out.println("hello2");break;
            case 3 : System.out.println("hello3");break;
            case 4 : System.out.println("hello4");break;
            case 5 : System.out.println("hello5");break;
           
        }

         sc.close();

       }

}

