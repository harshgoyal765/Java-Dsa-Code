public class assignment {
    public static void main(String args[]){
      int a=5;
      a=+10;//a=a+10
      System.out.println(a);
      int b=a;//(assign value of a to b)
      System.out.println(b);
      b-=10;//b=b-10
      System.out.println(b);
      int c=a;
      c*=10;//c=c*10
      System.out.println(c);
      int d=a;
      d/=10;//d=d/10
      System.out.println(d);
      int e=a;
      e+=10;//e=e+10
      System.out.println(e);

    }
}
